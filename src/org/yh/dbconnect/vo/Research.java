package org.yh.dbconnect.vo;

public class Research {
    private int res_id;
    private String res_name;
    private int res_fee;
    private String res_person;

    public int getRes_id() {
        return res_id;
    }

    public void setRes_id(int res_id) {
        this.res_id = res_id;
    }

    public String getRes_name() {
        return res_name;
    }

    public void setRes_name(String res_name) {
        this.res_name = res_name;
    }

    public int getRes_fee() {
        return res_fee;
    }

    public void setRes_fee(int res_fee) {
        this.res_fee = res_fee;
    }

    public String getRes_person() {
        return res_person;
    }

    public void setRes_person(String res_person) {
        this.res_person = res_person;
    }
}
